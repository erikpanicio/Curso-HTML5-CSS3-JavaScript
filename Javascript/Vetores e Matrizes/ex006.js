var frutas = ["Maçã", "Uva", "Laranja", "Banana"]

frutas.sort() /* organiza em ordem alfabética */
frutas.reverse() /* inverte a ordem, de trás para frente */

console.log(frutas)

/* como não há um função para mostrar em ordem decrecente, é necessário utilizar essas duas funções para obter o resutado */